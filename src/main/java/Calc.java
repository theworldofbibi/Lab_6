public class Calc {
    
    public double somar(double numA, double numB) {
        return numA + numB;
    }
    
    public double subtrair(double numA, double numB) {
        return numA - numB;
    }
    
    public double multiplicar(double numA, double numB) {
        return numA * numB;
    }
    
    public double dividir(double numA, double numB) {
        return numA / numB;
    }
    
}